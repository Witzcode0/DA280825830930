# False → Boolean value representing false.
# None → Represents absence of a value.

# True → Boolean value representing true.

# and → Logical operator (both conditions must be true).

# as → Used to create an alias (mainly in imports, exceptions).

# assert → Debugging check; raises error if condition is false.

# async → Declares a function as asynchronous.

# await → Waits for completion of an async function.

# break → Exits the nearest loop immediately.

# class → Defines a new class.

# continue → Skips current loop iteration and moves to next.

# def → Defines a new function.

# del → Deletes a variable, item, or attribute.

# elif → Else-if condition inside an if block.

# else → Executes code if if condition is false.

# except → Catches exceptions in try-except blocks.

# finally → Always executes code at the end of try-except.

# for → Loop that iterates over a sequence.

# from → Imports specific parts from a module.

# global → Declares a variable as global inside a function.

# if → Conditional statement.

# import → Loads a module.

# in → Checks membership in a sequence.

# is → Checks identity of two objects.

# lambda → Creates an anonymous (inline) function.

# nonlocal → Refers to variables in outer (non-global) scope.

# not → Logical negation operator.

# or → Logical operator (at least one condition true).

# pass → Empty placeholder statement (does nothing).

# raise → Raises an exception.

# return → Sends a value back from a function.

# try → Starts a block to test for exceptions.

# while → Repeats a block while condition is true.

# with → Context manager for resources (auto cleanup).

# yield → Returns value from a generator function.
