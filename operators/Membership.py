# Membership Operators:
# Used to test if a sequence contains a specific value.
# in (Returns True if a value is present in the sequence)
# not in (Returns True if a value is not present in the sequence)

name = "brijesh"
print("b" in name)
print("j" in name)
print("B" in name)
print("ij" in name)
print("ie" in name)
print("ie" not in name)
print("ij" not in name)